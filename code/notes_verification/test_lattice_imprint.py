# ============================================================
# Lattice distance
# ============================================================
def lattice_distance(r, primes=(2, 3), max_exp=5):
    """
    Compute the minimum log_2-distance from log_2(r) to the lattice
    generated by {log_2(p) : p in primes}, with integer coefficients
    bounded by max_exp.
    """
    log_r = np.log2(abs(r))
    log_primes = [np.log2(p) for p in primes]
    n = len(primes)
    best_eps = float('inf')
    
    if n == 2:
        for a in range(-max_exp, max_exp + 1):
            for b in range(-max_exp, max_exp + 1):
                eps = abs(log_r - (a*log_primes[0] + b*log_primes[1]))
                best_eps = min(best_eps, eps)
    elif n == 3:
        for a in range(-max_exp, max_exp + 1):
            for b in range(-max_exp, max_exp + 1):
                for c in range(-max_exp, max_exp + 1):
                    eps = abs(log_r - (a*log_primes[0] + b*log_primes[1] + c*log_primes[2]))
                    best_eps = min(best_eps, eps)
    elif n == 4:
        red = min(max_exp, 4)
        for a in range(-red, red + 1):
            for b in range(-red, red + 1):
                for c in range(-red, red + 1):
                    for d in range(-red, red + 1):
                        eps = abs(log_r - (a*log_primes[0] + b*log_primes[1]
                                          + c*log_primes[2] + d*log_primes[3]))
                        best_eps = min(best_eps, eps)
    return best_eps


