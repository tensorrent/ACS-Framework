def run_test(label, ratios_dict, primes_list, max_exp=5, n_null=5000, seed=42):
    """
    For each lattice in primes_list, compute mean lattice distance for
    the data and for a null distribution of N=n_null random ratios
    drawn uniformly in log scale over the data's log range.
    
    Reports: data mean, null mean, ratio, z-score.
    """
    actual_logs = [np.log2(r) for r in ratios_dict.values()]
    log_min = min(actual_logs) - 0.5
    log_max = max(actual_logs) + 0.5
    
    print(f"\n{'='*78}")
    print(f"  {label}")
    print(f"{'='*78}")
    print(f"  log_2 range: [{log_min:.2f}, {log_max:.2f}]")
    print(f"  N data points: {len(ratios_dict)}, N null draws: {n_null}")
    print()
    print(f"  {'Lattice':<25} {'Data':>10} {'Null':>10} {'Ratio':>8} {'Z-score':>10}")
    print(f"  {'-'*75}")
    
    for primes in primes_list:
        actual_eps = [lattice_distance(r, primes, max_exp) for r in ratios_dict.values()]
        
        np.random.seed(seed)
        rlogs = np.random.uniform(log_min, log_max, n_null)
        null_eps = [lattice_distance(2**lr, primes, max_exp) for lr in rlogs]
        
        data_mean = np.mean(actual_eps)
        null_mean = np.mean(null_eps)
        null_std = np.std(null_eps)
        ratio = data_mean / null_mean if null_mean > 0 else 0
        z = (data_mean - null_mean) / null_std if null_std > 0 else 0
        
        primes_str = f"<{','.join(str(p) for p in primes)}>"
        print(f"  {primes_str:<25} {data_mean:>10.4f} {null_mean:>10.4f} "
              f"{ratio:>8.2f} {z:>+10.2f}")
    
    print()


